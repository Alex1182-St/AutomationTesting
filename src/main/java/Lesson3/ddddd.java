package Lesson3;

public class ddddd {

    public static void main(String[] args) {


        int a, b, result = 0;

        for (a = 2; a < 10; a++) {

            for (b = 2; b < 10; b++) {

                result = a*b;

                System.out.println(a+"x"+b+" = "+result);

            }

        }




    }


}
